const simpleConditionalExample = function() {
    if (true) {
        // code in between the curly brackets will ONLY run
        // if the expression inside the (...) is true
        console.log('this will show up!');
    }

    if (false) {
        console.log('will this show up tho...?')
    }

    return 'this is a test';
}

simpleConditionalExample();




























